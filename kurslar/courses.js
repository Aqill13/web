//! dizayn next prev butonlarinin displayinin none ve ya block olmasi

const prevBtn_dizayn = document.querySelector("#dizayn_prev");
let slideCount_dizayn = 1;

function moveSlider_dizayn(direction) {
  if (direction === "next") {
    slideCount_dizayn++;
    prevBtn_dizayn.style.display = "block";
    if (slideCount_dizayn == 4) {
      document.querySelector("#dizayn_next").style.display = "none";
    }
  } else if (direction === "prev") {
    slideCount_dizayn--;
    document.querySelector("#dizayn_next").style.display = "block";
    if (slideCount_dizayn === 1) {
      prevBtn_dizayn.style.display = "none";
    }
  }
}