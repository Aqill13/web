const frontend_filter_btns = document.querySelectorAll(".frontend_filter_btn");
frontend_filter_btns.forEach((frontFilterBtn) => {
  frontFilterBtn.addEventListener("click", (e) => {
    const front_filter_btn_dataset = e.target.dataset.id;

    if (front_filter_btn_dataset === "en_meshur") {
      frontend_filter_display_course(frontendCourseCategory_enMeshur);
      document.querySelector(".frontend_course_content").scrollLeft = 0;
    } else if (front_filter_btn_dataset === "yeni") {
      frontend_filter_display_course(frontendCourseCategory_yeni);
      document.querySelector(".frontend_course_content").scrollLeft = 0;
    } else frontend_filter_display_course(frontendCourseCategory_trend);
    document.querySelector(".frontend_course_content").scrollLeft = 0;
  });
});

//! Front-end filter buttons

frontend_filter_btns.forEach((frontFilterBtn) => {
  frontFilterBtn.addEventListener("click", function (e) {
    frontend_filter_btns.forEach((active_remove_filter) => {
      active_remove_filter.classList.remove("frontend_active_filter_btn");
    });
    frontFilterBtn.classList.add("frontend_active_filter_btn");
    //todo: line
    const frontend_filter_btns_line =
      document.querySelector(".frontend_filter_btn_line");
    frontend_filter_btns_line.style.width = e.target.offsetWidth + "px";
    frontend_filter_btns_line.style.left = e.target.offsetLeft + "px";
  });
});

//! Front-end course scroll
const next_prev_btn = document.querySelectorAll(
  ".frontend_course_contents .frontend_course_content_box_next_prev"
);
const frontend_course_content = document.querySelector(
  ".frontend_course_content"
);

next_prev_btn.forEach((btn) => {
  btn.addEventListener("click", () => {
    frontend_course_content.scrollLeft +=
      btn.id === "frontend_prev" ? -700 : 700;
  });
});

//! fronend next prev butonlarinin displayinin none ve ya block olmasi

const prevBtn = document.querySelector("#frontend_prev");
let slideCount = 1;

function moveSlider_frontend(direction) {
  if (direction === "next") {
    slideCount++;
    prevBtn.style.display = "block";
    if (slideCount == 4) {
      document.querySelector("#frontend_next").style.display = "none";
    }
  } else if (direction === "prev") {
    slideCount--;
    document.querySelector("#frontend_next").style.display = "block";
    if (slideCount === 1) {
      prevBtn.style.display = "none";
    }
  }
}

